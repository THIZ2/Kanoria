<p>Kanoria PG Mahila Mahavidyalaya was established in 1965 by Late Bhagirath Kanoria in aesthetic environs at Jawahar Lal Nehru Marg as the first institution of higher education for women through grant-in-aid from the Government of Rajasthan. In its 54 years old journey our college has achieved many milestones. It is a leading college not only of the city but also of the state of Rajasthan in imparting excellent education, enabling holistic development and inculcating leadership and social values. The college has earned a prestigious reputation of harnessing a progressive outlook towards education by introducing new courses at UG and PG level, from time-to-time, skill enhancement and (21st Century for skills) career oriented courses, ICT enabled teaching facilities, outreach activities and discipline. About 6500 students enroll every year in 24 subjects in Science, Arts and Commerce streams for Bachelor's programme and 11 Master's programme. Maintaining a lead in premier educational location, the college has clocked an enviable growth keeping abreast with the needs and aspirations of times.</p>


<p>Sprawling lush green lawns adorned with ornamental trees beautify the campus and make it conducive to learning. A well maintained botanical garden, nursery and the abounding greenery also protect the biodiversity of the area. Infrastructure facilities and amenities in the college add the effective and efficient conduct of all educational programmes.
The pivot around which all initiatives and efforts center is the 'student' as the future of the college. The college offers degree programs at the undergraduate and post graduate level in Arts, Commerce and Science. Additionally, Skill Enhancement Courses, Diploma and certificate courses provides students opportunities to step out in the world skilled, well-groomed and prepared to create an identity for themselves.</p>
